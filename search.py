import pandas as pd

# Завантаження файлів
df1 = pd.read_csv('/Users/macbook/Documents/диплом/Я ніколи не.csv', encoding='utf-8-sig', sep=';')
df2 = pd.read_csv('/Users/macbook/Documents/диплом/Рівердейл.csv', encoding='utf-8-sig', sep=';')

# Знаходження спільних слів
common = set(df1['Англійський сленговий вираз']) & set(df2['Англійський сленговий вираз'])
# Виведення кількості знайдених спільних виразів
print(f"Found {len(common)} common expressions:\n")
# Пошук по всіх спільних виразах у алфавітному порядку
for expression in sorted(common):
    # Виведення виразу
    print(f"\n {expression}")
    # Пошук усіх рядків із цим виразом у першому серіалі
    rows1 = df1[df1['Англійський сленговий вираз'] == expression]
    # Пошук усіх рядків із цим виразом у другому серіалі
    rows2 = df2[df2['Англійський сленговий вираз'] == expression]
    # Виведення інформації для серіалу "Never Have I Ever"
    print(f"\n    Я ніколи не ({len(rows1)} варіант):")
    # Перебір усіх варіантів перекладу
    for i, row in rows1.iterrows():
        # Виведення українського перекладу
        print(f"      {i + 1}. Переклад: \"{row['Український відповідник']}\"")
        # Виведення англійського контексту
        print(f"         Контекст (Англ): {row['Сленговий вираз у контексті (англійською)']}")
        # Виведення українського контексту
        print(f"         Контекст (Укр): {row['Сленговий вираз у контексті (українською)']}")

    # Виведення інформації для серіалу "Riverdale"
    print(f"\n   Рівердейл ({len(rows2)} варіант):")
    # Перебір усіх варіантів перекладу
    for i, row in rows2.iterrows():
        # Виведення українського перекладу
        print(f"      {i + 1}. Переклад: \"{row['Український відповідник']}\"")
        # Виведення англійського контексту
        print(f"         Кнтекст (Англ): {row['Сленговий вираз у контексті (англійською)']}")
        # Виведення українського контексту
        print(f"         Контекст (Укр): {row['Сленговий вираз у контексті (українською)']}")

    print("\n" + "-" * 100)

# Збереження результатів у новий CSV-файл
if common:
    # Список для майбутніх результатів
    result = []
    # Пошук по кожному спільному виразу
    for expression in sorted(common):
        # Додавання всіх варіантів з першого серіалу
        for _, row in df1[df1['Англійський сленговий вираз'] == expression].iterrows():
            result.append({
                'Сленгове слово': expression,
                'Серіал': 'Never Have I Ever',
                'Переклад українською': row['Український відповідник'],
                'Контекст (англ)': row['Сленговий вираз у контексті (англійською)'],
                'Контекст (укр)': row['Сленговий вираз у контексті (українською)']
            })

        # Додавання всіх варіантів з першого серіалу
        for _, row in df2[df2['Англійський сленговий вираз'] == expression].iterrows():
            result.append({
                'Сленгове слово': expression,
                'Серіал': 'Riverdale',
                'Переклад українською': row['Український відповідник'],
                'Контекст (англ)': row['Сленговий вираз у контексті (англійською)'],
                'Контекст (укр)': row['Сленговий вираз у контексті (українською)']
            })
    # Створення DataFrame з результатів
    df_result = pd.DataFrame(result)
    # Збереження DataFrame у CSV-файл
    df_result.to_csv('common_expressions_each_variant.csv', index=False, encoding='utf-8-sig')
    print(f"\n Saved to file: common_expressions_each_variant.csv")
